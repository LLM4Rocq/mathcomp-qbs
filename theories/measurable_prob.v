(* Section 7: Summary and the full bridge                              *)
(* ================================================================== *)

(* SUMMARY
   ========

   The space pprobability T R of probability measures on a measurable
   type T already has a sigma-algebra in mathcomp-analysis (generated
   by pset = {mset U r | U measurable, r in [0,1]}). We proved:

   1. EVALUATION MAPS ARE MEASURABLE (eval_meas):
      For measurable U, the map P |-> P(U) is measurable from
      pprobability T R to \bar R.

   2. COMPOSITION PRESERVES MEASURABILITY (eval_comp_meas):
      If f : X -> pprobability T R is measurable, then for any
      measurable U, x |-> f(x)(U) is measurable.

   3. POINTWISE <=> FUNCTION MEASURABILITY (pprobability_meas_iff_kernel):
      g : X -> probability T R is measurable as X -> pprobability T R
      if and only if x |-> g(x)(U) is measurable for each measurable U.

   4. QBS BRIDGE (kernel_hyp_equiv):
      For f : mR -> qbs_prob(R_qbs mR) satisfying the strong condition
      with witness g, qbs_mu_measurable_kernel(f) is equivalent to
      pointwise kernel measurability of g.

   5. GIRY KERNEL REDUCTION (giry_kernel_from_g_kernel):
      For a general target M, kernel measurability of the Giry image
      qbs_to_giry(f(alpha_p(r)))(U) reduces to kernel measurability
      of g on alpha_Y-preimages (which are fixed measurable sets).

   6. SPECIFIC INSTANCES:
      - Dirac family: always measurable (dirac_family_measurable)
      - Constant family: always measurable (const_family_measurable)
      - Dirac composed with measurable function: measurable
        (dirac_comp_family_measurable)

   The key architectural insight is that pprobability measurability
   and pointwise kernel measurability are EQUIVALENT
   (pprobability_meas_iff_kernel). This means:
   - The hypothesis qbs_mu_measurable_kernel from qbs_fubini.v
     can be reformulated as measurability into pprobability.
   - kprobability from kernel.v can be applied once we have
     pprobability measurability.
   - For specific constructions (return, sample), measurability
     can be verified directly without any hypothesis. *)
