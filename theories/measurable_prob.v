(* # Measurable Structure on Probability Measures               *)
(*                                                               *)
(* This file formalizes the measurable structure on the space    *)
(* of probability measures and connects it to QBS.              *)
(*                                                               *)
(* mathcomp-analysis 1.16.0 already provides:                   *)
(* - [pprobability T R] as a measurableType with sigma-algebra  *)
(*   generated by [pset] = {mset U r | U measurable, r in [0,1]}*)
(* - [kprobability] which builds a probability kernel from a    *)
(*   measurable function to pprobability                         *)
(*                                                               *)
(* We prove:                                                     *)
(* ```                                                           *)
(*   eval_meas == for measurable U, the evaluation map           *)
(*     P |-> P(U) is measurable from pprobability to \bar R     *)
(*   eval_comp_meas == if f : X -> pprobability T R is           *)
(*     measurable, then x |-> f(x)(U) is measurable             *)
(*   pointwise_to_pprobability_meas == pointwise kernel          *)
(*     measurability implies function measurability into          *)
(*     pprobability                                               *)
(*   pprobability_to_pointwise_meas == the converse               *)
(*   pprobability_meas_iff_kernel == the equivalence              *)
(*   giry_kernel_from_g_kernel == kernel measurability on M      *)
(*     reduces to kernel measurability on mR via alpha_Y         *)
(*   giry_family_measurable == the Giry image family is          *)
(*     measurable as mR -> pprobability M R                       *)
(*   kernel_hyp_equiv == qbs_mu_measurable_kernel is equivalent  *)
(*     to pointwise kernel measurability of g                     *)
(*   dirac_family_measurable == the Dirac family is measurable   *)
(*   const_family_measurable == constant families are measurable *)
(*   return_kernel_measurable == Dirac kernel from a measurable  *)
(*     function is measurable                                     *)
(* ```                                                           *)
(*****************************************************************)

