(* Section 6: Instances for specific constructions                     *)
(* ================================================================== *)

(* For specific QBS constructions, we can verify the kernel
   measurability hypothesis directly, without assuming it. *)

Section specific_instances.
Variable R : realType.
Local Notation mR := (measurableTypeR R).

(* The Dirac family r |-> dirac(r) is measurable as
   mR -> pprobability mR R *)
Lemma dirac_family_measurable :
  measurable_fun [set: mR] (fun r : mR => dirac r : pprobability mR R).
Proof.
apply: pointwise_to_pprobability_meas => U mU.
(* dirac(r)(U) = (\1_U r)%:E = EFin (indic U r) *)
have -> : (fun x : mR => (dirac x : measure _ _) U) =
          EFin \o (fun r : mR => numfun.indic U r : R).
  by apply: boolp.funext => r; rewrite /= /dirac.
apply/measurable_EFinP.
apply: measurable_indic => //.
Qed.

(* Kernel measurability of the Dirac family *)
Lemma dirac_kernel_measurable (U : set mR) (mU : measurable U) :
  measurable_fun setT (fun r : mR => @dirac _ mR r R U).
Proof.
have := pprobability_to_pointwise_meas dirac_family_measurable mU.
suff -> : (fun x : mR => (dirac x : probability mR R) U) =
          (fun x : mR => @dirac _ mR x R U) by [].
by [].
Qed.

(* For a constant family (e.g., from sample): *)
Lemma const_family_measurable (P : probability mR R) :
  measurable_fun [set: mR] (fun _ : mR => P : pprobability mR R).
Proof.
exact: measurable_cst.
Qed.

(* Kernel measurability of the constant family *)
Lemma const_kernel_measurable (P : probability mR R) (U : set mR) :
  measurable U ->
  measurable_fun setT (fun _ : mR => (P : measure _ _) U).
Proof.
by move=> _; exact: measurable_cst.
Qed.

(* For qbs_return composed with a measurable function:
   g(r) = dirac(h(r)) for measurable h : mR -> mR.
   The kernel measurability: r |-> dirac(h(r))(U) = 1_U(h(r))
   is measurable because h is measurable and 1_U is measurable. *)

Lemma return_kernel_measurable (h : mR -> mR)
    (hm : measurable_fun setT h) (U : set mR) (mU : measurable U) :
  measurable_fun setT (fun r : mR => @dirac _ mR (h r) R U).
Proof.
(* dirac(h(r))(U) = (\1_U (h r))%:E = EFin (indic U (h r)) *)
have -> : (fun r : mR => @dirac _ mR (h r) R U) =
          EFin \o (numfun.indic U \o h).
  by apply: boolp.funext => r; rewrite /= /dirac.
apply/measurable_EFinP.
apply: measurableT_comp; last exact: hm.
exact: measurable_indic.
Qed.

(* The Dirac family composed with a measurable function *)
Lemma dirac_comp_family_measurable (h : mR -> mR)
    (hm : measurable_fun setT h) :
  measurable_fun [set: mR]
    (fun r : mR => @dirac _ mR (h r) R : pprobability mR R).
Proof.
apply: pointwise_to_pprobability_meas.
exact: return_kernel_measurable hm.
Qed.

End specific_instances.

